<h1>Bienvenue</h1>
<!-- Contenu qui explique le principe du système qui permet au docteur de gérer ses patients -->
<p>Le principe du système est le suivant : </p>
<ul>
    <li>Le docteur se connecte à son compte</li>
    <li>Il peut ajouter un patient</li>
    <li>Il peut consulter la liste de ses patients</li>
    <li>Il peut consulter les informations d'un patient</li>
    <li>Il peut modifier les informations d'un patient</li>
    <li>Il peut supprimer un patient</li>
</ul>
